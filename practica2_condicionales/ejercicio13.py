# Escribir un programa que pida al usuario dos enteros y que luego muestre si el primero es
# mayor que el segundo o viceversa.
primer_numero = int(input("Ingrese el primer número entero "));
segundo_numero = int(input("Ingrese el segundo número entero "));

if primer_numero > segundo_numero :
    print("El primer número es mayor",primer_numero);
else :
    print("El segundo número es mayor",segundo_numero);