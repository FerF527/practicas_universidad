# Escribir en papel un programa que pida al usuario dos números y muestre por pantalla su promedio.

primer_numero = input("Ingrese el primer número: ");
segundo_numero = input("Ingrese el segundo número: ");
primer_numero = float(primer_numero);
segundo_numero = float(segundo_numero);
promedio = (primer_numero + segundo_numero) / 2;
print("El promedio de los dos números es: ");
print(promedio);