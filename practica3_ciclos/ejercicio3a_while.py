# a) Hacer un programa que muestre, mediante un ciclo, los 5 números naturales que le
# siguen al 10 (11, 12, · · · , 15).
numero_inicial = 11;
print("Mostrando 5 primeros números naturales que le siguen a 10");
while numero_inicial <= 15 :
    print(numero_inicial);
    numero_inicial = numero_inicial + 1;